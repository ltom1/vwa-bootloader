//==================================================
// mem_set: sets <n_bytes> bytes at <dest> to <val>
//==================================================

void mem_set(u8 *dest, u8 val, u32 n_bytes) {

    for (u32 i = 0; i < n_bytes; i++) {
        *(dest + i) = val;
    }
}


//==================================================
// mem_cpy: copies <n_bytes> bytes from <src> to <dest>
//==================================================

void mem_cpy(u8 *dest, u8 *src, u32 n_bytes) {

    for (u32 i = 0; i < n_bytes; i++) {
          *(dest + i) = *(src + i);
     }
}


